    private void cargarTablaArticulos(String indexE) {

        try {

            DefaultTableModel tabla = new DefaultTableModel(Configuracion.listaArticulosIndispensables.getMatrizProductoPorCategoria(indexE),
                    Configuracion.listaArticulosIndispensables.getTituloProducto());
            
            
            this.tablaArticulos.setModel(tabla);
            
            dimensionFilas(tablaArticulos);
        
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR: " + e);
        }

           
        
    }

