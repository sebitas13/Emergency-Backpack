    private void ingresarPesoMaximo(){
        int peso=15;
         
        try {
            do{
              
           peso = Integer.parseInt(JOptionPane.showInputDialog(null, "INGRESE EL  PESO MAXIMO DE LA CAJA"));
               
            }while(peso<9 || peso>20); 
        } catch (Exception e) {
            
            System.out.println("ERROR: "+e);
            
        }
        
       this.pesoMaximo= peso;
       this.botones(true);
    }
    
